/*
*   int add(int addend1, int addend2)
*   @para{int} addend1 -- one addend of expression
*   @para{int} addend2 -- second addend of expression
*
*   @return{int} the computed result of addend1 + addend2
*   j.montgomery
*/
int add(int, int);

/*
*   int sub(int minuend, int subtrahend)
*   @para{int} minuend -- left parameter of subtraction expression
*   @para{int} subtrahend -- right parameter of subtraction expression
*
*   @return{int} the computed result of minuend - subtrahend
*/
int sub(int, int);
